import logging
import threading
from weakref import ref
import tornado.websocket
import tornado.web
import tornado.ioloop
from perspective import Table, PerspectiveManager, PerspectiveTornadoHandler
import threading
import concurrent.futures
import pandas as pd

IS_MULTI_THREADED = True
WELL_LOG_DATA_PATH = "archive/well_log.csv"
MAX_ROWS = 200


class State:
    idx: int
    df: pd.DataFrame

    def __init__(self, df) -> None:
        self.idx = 1
        self.df = df


def perspective_thread(manager):
    """Perspective application thread starts its own tornado IOLoop, and
    adds the table with the name "data_source_one", which will be used
    in the front-end."""
    table = Table(
        {
            "Depth": int,
            "Gamma_ray": float,
            "Shale_Volume": float,
            "Restivity": float,
            "Delta_T": float,
            "T_Offset": float,
            "Vp": float,
            "Vs": float,
            "Density": float,
            "Density_Calculated": float,
            "Neuron_Porosity": float,
            "Density_Porosity": float,
            "Possions_Ratio": float,
            "Classification": int,
        },
        limit=MAX_ROWS,
    )

    # Track the table with the name "data_source_one", which will be used in
    # the front-end to access the Table.
    manager.host_table("data_source_one", table)
    df = pd.read_csv(WELL_LOG_DATA_PATH)
    df["Delta_T"] = df["Delta_T"].astype(float)
    df["T_Offset"] = df["Delta_T"].cumsum()
    state = State(df)

    # update with new data every 50ms
    def updater():
        # loopback
        if state.idx >= len(state.df):
            state.idx = 1

        r = state.idx
        l = max(r - MAX_ROWS, 0)
        data = state.df[l:r]
        table.update(data)
        state.idx += 1

    callback = tornado.ioloop.PeriodicCallback(
        callback=updater, jitter=0.1, callback_time=100
    )
    psp_loop = tornado.ioloop.IOLoop()

    if IS_MULTI_THREADED:
        with concurrent.futures.ThreadPoolExecutor() as executor:
            manager.set_loop_callback(psp_loop.run_in_executor, executor)
            callback.start()
            psp_loop.start()
    else:
        manager.set_loop_callback(psp_loop.add_callback)
        callback.start()
        psp_loop.start()


def make_app():
    manager = PerspectiveManager()
    thread = threading.Thread(target=perspective_thread, args=(manager,))
    thread.daemon = True
    thread.start()

    return tornado.web.Application(
        [
            # create a websocket endpoint that the client Javascript can access
            (
                r"/websocket",
                PerspectiveTornadoHandler,
                {"manager": manager, "check_origin": True},
            ),
            (
                r"/node_modules/(.*)",
                tornado.web.StaticFileHandler,
                {"path": "./node_modules/"},
            ),
            (
                r"/(.*)",
                tornado.web.StaticFileHandler,
                {"path": "./", "default_filename": "index.html"},
            ),
        ]
    )


def main():
    app = make_app()
    app.listen(8080)
    logging.critical("Listening on http://localhost:8080")
    loop = tornado.ioloop.IOLoop.current()
    loop.start()


if __name__ == "__main__":
    main()
